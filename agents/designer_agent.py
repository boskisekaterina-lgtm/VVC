"""
Агент 2 — Дизайнер сайта и презентации.
Формирует структуру и контент для внешних материалов ВВС.
"""

import anthropic

SYSTEM_PROMPT = """Ты — Дизайнер контента для дивизиона ВВС («Вход в сети»).

ВВС — сервис по вводу поставщиков в торговые сети. Целевая аудитория:
поставщики и производители, которые хотят войти в торговые сети, но не могут
самостоятельно добраться до подписанного договора.

Ключевые факты о ВВС:
- 74 сети в работе (64 food + 10 non-food): Магнит, X5, Лента, Ашан, ВкусВилл, Самокат,
  Метро, Окей, Глобус, Азбука, Детский Мир, Галамарт, Fix Price, Роснефть АЗС и др.
- Некопируемое УТП: личные связи с байерами и коммерческими директорами сетей
- Управляемый маршрут входа с фиксируемыми результатами на каждом этапе
- Структурированная обратная связь от сети по чек-листу из 12 пунктов

Дискретная воронка (фиксированные цены за этап):
| Этап | Результат | Стоимость |
|------|-----------|-----------|
| 1 | Аудит КП | 50 000 руб. |
| 2 | Ответ от сети по чек-листу (12 пунктов) | 50 000 руб. |
| 3 | Предварительная договорённость | 150 000 руб. |
| 4 | Подписанный договор с сетью | 300 000 — 500 000 руб. |

Продолженная воронка: % от оборота (2–15%) на год, или смешанная модель.

3 ключевых конкурентных преимущества:
1. Личный доступ к ЛПР — не в общий ящик, а конкретному байеру
2. Структурированный ответ сети по 12 пунктам (что интересно, что изменить, почему нет)
3. Фиксированная цена каждого шага — платишь за результат, не за абстрактное сопровождение

Типичный клиент ВВС: поставщик, участвовавший в ЦЗС (Центр закупок сетей),
получивший интерес от сетей, но не заключивший контракт. 20 переговоров, 15 интересов —
и ни одного договора.

Твоя задача: создать структуру и копирайт для:
1. Сайта ВВС (6 страниц)
2. Презентации для поставщиков (9 слайдов)

Пиши конкретно: заголовки, подзаголовки, ключевые тезисы и примеры текстов для каждого блока.
Тон: деловой, конкретный, без воды. Целевая аудитория — собственники производств и коммерческие директора.
"""


def run_designer(focus: str | None = None) -> str:
    """
    Запускает агента-дизайнера контента.

    Args:
        focus: Фокус задачи — 'site', 'presentation' или None (оба материала).

    Returns:
        Структура и копирайт для сайта и/или презентации.
    """
    client = anthropic.Anthropic()

    if focus == "site":
        task = "Создай только структуру и тексты для сайта ВВС (6 страниц)."
    elif focus == "presentation":
        task = "Создай только структуру и тексты для презентации ВВС (9 слайдов)."
    else:
        task = "Создай структуру и тексты для сайта ВВС (6 страниц) и презентации (9 слайдов)."

    user_message = f"""{task}

Для каждой страницы сайта укажи:
- Название страницы
- Главный заголовок (H1)
- Подзаголовок или лид-текст
- Ключевые блоки на странице
- Примеры конкретных текстов (не заглушки, а реальный копирайт)
- CTA (призыв к действию), если применимо

Для каждого слайда презентации укажи:
- Заголовок слайда
- Основной тезис (1–2 предложения)
- Ключевые пункты (bullet points)
- Визуальная идея (что показать на слайде)

Важно: не используй шаблонный язык. Каждый элемент должен быть конкретным,
основанным на реальных данных ВВС: цифрах, сетях, условиях воронки."""

    print("Агент-дизайнер запущен...\n")

    with client.messages.stream(
        model="claude-opus-4-8",
        max_tokens=6000,
        thinking={"type": "adaptive"},
        system=SYSTEM_PROMPT,
        messages=[{"role": "user", "content": user_message}],
    ) as stream:
        result = stream.get_final_message()

    output = ""
    for block in result.content:
        if block.type == "text":
            output += block.text

    return output
