"""
Агент 1 — Аналитик.
Анализирует клиентскую базу и формирует таблицу передачи сетей
от Вартана к Подлеснову и Лапшиной с оценкой раппорта.
"""

import anthropic

SYSTEM_PROMPT = """Ты — Аналитик дивизиона ВВС («Вход в сети»).

ВВС — сервис по вводу поставщиков в торговые сети. Дивизион работает с 74 сетями
(64 food + 10 non-food). Ключевой партнёр по сетям — Вартан (ведёт ~90% food-сетей,
работает 50/50 по доходу). Менеджеры дивизиона — Подлеснов и Лапшина.

Распределение сетей:
- Вартан один: 34 сети (преимущественно food)
- Вартан совместно с Подлесновым/Лапшиной: 19 сетей
- Подлеснов/Лапшина самостоятельно: 8 сетей
- Другие (Циммермайн, Серикова, Андрей): остальные

ABC-классификация:
- A — стратегические сети (персональное ведение, программа касаний)
- B — поддерживать контакт, подключать при наличии ресурса
- C — не брать в активный контур

Уровни раппорта (доверие к ЛПР сети):
слабый → рабочий → сильный → стратегический

Статусы передачи: не начата / в процессе / завершена

Твоя задача: на основе входных данных о сетях сформировать таблицу передачи клиентов
от Вартана к Подлеснову и Лапшиной. Для каждой сети оцени риски при передаче
и предложи конкретный следующий шаг.

Формат вывода — строго markdown-таблица:

| Сеть | Категория | Текущий ответственный | Передаётся кому | Статус передачи | Раппорт до | Раппорт после | Риски | Следующий шаг |
|------|-----------|----------------------|-----------------|-----------------|------------|---------------|-------|---------------|

После таблицы — краткий раздел «Приоритеты» с топ-3 критическими передачами
и конкретными действиями на следующую неделю.
"""


def run_analyst(networks_data: str | None = None) -> str:
    """
    Запускает агента-аналитика.

    Args:
        networks_data: Описание сетей и текущего состояния в свободной форме.
                       Если None — агент использует типовые данные из CLAUDE.md.

    Returns:
        Markdown-таблица с анализом передачи клиентов.
    """
    client = anthropic.Anthropic()

    if networks_data is None:
        networks_data = """
Передай анализ для следующих сетей, где идёт или планируется передача от Вартана:

Food-сети (совместное ведение Вартан + Подлеснов/Лапшина):
1. Магнит — все форматы (Магнит, Магнит Косметик, Магнит Аптека)
2. X5 Retail (Пятёрочка, Перекрёсток, Чижик)
3. Лента
4. Окей
5. Ашан
6. ВкусВилл
7. Самокат
8. Метро
9. Глобус
10. Азбука Вкуса

Non-food сети (в работе):
11. Детский Мир
12. Галамарт
13. Fix Price

По каждой сети укажи реалистичную оценку раппорта и рисков передачи.
Используй знание о том, что Вартан — ключевой носитель личных связей с ЛПР,
и при передаче уровень раппорта, как правило, снижается на один уровень.
"""

    user_message = f"""Сформируй таблицу передачи клиентов на основе следующих данных:

{networks_data}

Важно:
- Учти, что передача раппорта — главный риск: Вартан знает байеров лично,
  Подлеснов и Лапшина знакомятся заново.
- Для сетей категории A рекомендуй совместные встречи Вартан + новый ответственный.
- Для сетей категории B — постепенная передача через email-копии и звонки.
- Для сетей категории C — можно передавать без дополнительных мер."""

    print("Агент-аналитик запущен...\n")

    with client.messages.stream(
        model="claude-opus-4-8",
        max_tokens=4000,
        thinking={"type": "adaptive"},
        system=SYSTEM_PROMPT,
        messages=[{"role": "user", "content": user_message}],
    ) as stream:
        result = stream.get_final_message()

    output = ""
    for block in result.content:
        if block.type == "text":
            output += block.text

    return output
