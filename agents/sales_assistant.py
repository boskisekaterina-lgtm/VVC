"""
Агент 3 — Ассистент по продажам.
Генерирует и адаптирует скрипт разговора с новым поставщиком
на основе профиля клиента.
"""

import anthropic

SYSTEM_PROMPT = """Ты — Ассистент по продажам дивизиона ВВС («Вход в сети»).

ВВС — сервис по вводу поставщиков в торговые сети. Ты помогаешь менеджерам
вести первый разговор с потенциальным клиентом.

Контекст бизнеса:
- Целевой клиент: поставщик/производитель, который участвовал в ЦЗС (Центр закупок сетей),
  получил интерес от сетей, но не заключил контракт. Типичная ситуация: 20 переговоров,
  15 сетей проявили интерес — и ни одного договора.
- Первый контакт — холодный или тёплый звонок от менеджера ВВС.
- Задача звонка: выявить точку застревания → предложить конкретный этап сервиса ВВС.

Дискретная воронка ВВС:
1. Аудит КП — 50 000 руб. (3–5 дней, понимаем готовность к сети)
2. Ответ от сети по чек-листу — 50 000 руб. (12 пунктов: что интересно, что изменить)
3. Предварительная договорённость — 150 000 руб.
4. Подписанный договор — 300 000–500 000 руб.

3 ключевых отличия ВВС от «обычных посредников»:
1. Личный доступ к ЛПР — не в общий ящик, а конкретному байеру лично
2. Структурированный ответ сети по 12 пунктам — конкретно, почему да или нет
3. Фиксированная цена этапа — платишь за результат, не за «сопровождение»

Работа с возражениями:
- «Пробовали сами» → разница в уровне диалога: не контакт, а предметное рассмотрение КП
- «Дорого» → 50 000 = цена одного дня переговоров, которые снова ни к чему не привели
- «Нужно подумать» → уточнить конкретное сомнение (цена / формат / логика)

Твоя задача: создать скрипт разговора, адаптированный под профиль конкретного поставщика.
Если дан профиль клиента — адаптируй каждый этап под его ситуацию.
Если профиля нет — создай универсальный скрипт.

Формат вывода: 6 этапов с точными репликами менеджера и ветками ответов поставщика.
После скрипта — раздел «Подготовка к звонку» с 3–5 вопросами, которые менеджер
должен выяснить до звонка (через базу ЦЗС или открытые источники).
"""


def run_sales_assistant(supplier_profile: str | None = None) -> str:
    """
    Запускает агента-ассистента по продажам.

    Args:
        supplier_profile: Профиль поставщика для адаптации скрипта.
                          Формат: название компании, категория товара, участие в ЦЗС,
                          известные проблемы/ситуация. Если None — универсальный скрипт.

    Returns:
        Готовый скрипт разговора с поставщиком.
    """
    client = anthropic.Anthropic()

    if supplier_profile:
        user_message = f"""Создай адаптированный скрипт разговора для следующего поставщика:

{supplier_profile}

Адаптируй каждый этап скрипта под конкретную ситуацию этого поставщика:
- Открытие: упомяни конкретные детали из его профиля (сети, дата ЦЗС, категория)
- Диагностика: сфокусируйся на вероятных точках застревания исходя из профиля
- Выгоды: расставь акценты на тех преимуществах ВВС, которые наиболее релевантны
- Предложение: выбери первый этап воронки, который подходит для его ситуации
- Возражения: подготовь ответы на наиболее вероятные возражения именно для него
- Закрытие: предложи конкретный следующий шаг с учётом его готовности"""
    else:
        user_message = """Создай универсальный скрипт разговора с новым поставщиком —
участником ЦЗС, который получил интерес от сетей, но не заключил контракт.

Скрипт должен:
1. Быть готов к использованию «как есть» — без заглушек вида [имя] кроме имени
2. Содержать точные реплики для каждого этапа
3. Включать ветки ответов на да/нет от поставщика
4. Быть написан живым деловым языком (не канцелярским)

Завершай каждый этап чётким переходом к следующему."""

    print("Агент-ассистент по продажам запущен...\n")

    with client.messages.stream(
        model="claude-opus-4-8",
        max_tokens=5000,
        thinking={"type": "adaptive"},
        system=SYSTEM_PROMPT,
        messages=[{"role": "user", "content": user_message}],
    ) as stream:
        result = stream.get_final_message()

    output = ""
    for block in result.content:
        if block.type == "text":
            output += block.text

    return output
